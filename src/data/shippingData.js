// Create a lookup object for checkout calculations
export const createShippingLookup = () => {
  const lookup = {};
  
  stopdeskFees.forEach(item => {
    const key = `${item.wilaya}-${item.commune}`;
    if (!lookup[key]) {
      lookup[key] = {};
    }
    lookup[key].stopdesk = item.prix;
  });

  domicileFees.forEach(item => {
    const key = `${item.wilaya}-${item.commune}`;
    if (!lookup[key]) {
      lookup[key] = {};
    }
    lookup[key].domicile = item.prix;
  });

  return lookup;
};

// Get unique wilayas
export const getWilayas = () => {
  const wilayas = [...new Set([...stopdeskFees.map(f => f.wilaya), ...domicileFees.map(f => f.wilaya)])];
  return wilayas.sort();
};

// Get communes for a specific wilaya
export const getCommunesByWilaya = (wilaya) => {
  const stopdeskCommunes = stopdeskFees.filter(f => f.wilaya === wilaya).map(f => ({ commune: f.commune, wilaya: f.wilaya }));
  const domicileCommunes = domicileFees.filter(f => f.wilaya === wilaya).map(f => ({ commune: f.commune, wilaya: f.wilaya }));
  
  // Combine and remove duplicates based on commune name
  const allCommunes = [...stopdeskCommunes, ...domicileCommunes];
  const uniqueCommunes = allCommunes.filter((commune, index, self) => 
    index === self.findIndex(c => c.commune === commune.commune)
  );
  
  return uniqueCommunes.sort((a, b) => a.commune.localeCompare(b.commune));
};

